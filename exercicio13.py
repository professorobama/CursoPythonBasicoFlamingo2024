#Faça um programa que receba três notas de um aluno e calcule sua média.

nota01 =float(input("Digite sua 1º nota :"))
nota02 =float(input("Digite sua 2º nota :"))
nota03 =float(input("Digite sua 3º nota :"))
media = (nota01 + nota02 + nota03)/3
print("Sua média é : ",round(media,2))